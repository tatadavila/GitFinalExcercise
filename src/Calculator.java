public class Calculator {
    public int Sum(int a, int b) {
        return a + b;
    }

    public int substract(int a, int b) {
        return a - b;
    }

    public int multiplication(int a, int b) {
        return a * b;
    }

    public float division(float a, float b) {
        return a / b;
    }

}
